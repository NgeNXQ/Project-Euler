        /*
            The sum of the primes below 10 is 2 + 3 + 5 + 7 = 17.
            Find the sum of all the primes below two million.
        */

        private static ulong GetSumOfPrimes(int number)
        {
            ulong result = 0;

            for (int i = 1; i < number; ++i)
            {
                if (IsPrime(i))
                    result += (ulong)i;
            }

            bool IsPrime(int number)
            {
                if (number == 1)
                    return false;
                else if (number == 2)
                    return true;
                else
                {
                    for (int i = 2, length = number; i < length; ++i)
                    {
                        if (number % i == 0)
                            return false;
                    }
                }

                return true;
            }

            return result;
        }

