        static int GetEvenNumbersFromFibonacciSequence(int number)
        {
            int result = 0;

            int firstNumber = 1;
            int secondNumber = 2;

            int temp;

            while (secondNumber < number)
            {
                if (secondNumber % 2 == 0)
                    result += secondNumber;

                temp = secondNumber;
                secondNumber = firstNumber + secondNumber;
                firstNumber = temp;
            }

            return result;
        }
